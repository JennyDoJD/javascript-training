let x = 10;

// the addition operator (+)
const addition = x + 2;

// the subtraction operator (-)
const subtraction = x - 3;

// the division operator (/)
const division = x / 2;

// the remainder operator (%)
const remainder = x % 4;

// the multiplication operator (*)
const multiplication = x * 5;

// the exponentiation operator (**)
const exponentiation = x ** 2;

console.log(
  addition +
    '\n' +
    subtraction +
    '\n' +
    division +
    '\n' +
    remainder +
    '\n' +
    multiplication +
    '\n' +
    exponentiation +
    '\n'
);
